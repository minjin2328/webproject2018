package com.domain;

public class Login {

		private String key, name_, pw_;

		public String getKey() {
			return key;
		}

		public void setKey(String key) {
			this.key = key;
		}

		public String getName_() {
			return name_;
		}

		public void setName_(String name_) {
			this.name_ = name_;
		}

		public String getPw_() {
			return pw_;
		}

		public void setPw_(String pw_) {
			this.pw_ = pw_;
		}
		
	
}
